package lesson02_DataTypeAndScanner;

public class Variables {
    public static void main(String[] args) {


        // Variable
        //Declaration (int schoolNo) | (= 145;) Assignment
               int schoolNo                  = 145;
        System.out.println(schoolNo);

        schoolNo = 995;
        System.out.println("School No: " + schoolNo);

        int not = 70;
        not = not + 10;
        System.out.println(not);

        double bankAccount = 5000;
        bankAccount = bankAccount - 467;
        System.out.println("Bank Account: " + bankAccount);
    }
}
