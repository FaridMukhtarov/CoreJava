package lesson02_DataTypeAndScanner;

public class DataTypePrimitives {
    public static void main(String[] args) {

        // Primitive data type containing integers
        byte b = 100;
        short s = 12234;
        int i = 2234434;
        long l = 637267618;

        // Primitive data type decimal
        double d = 122.4;
        float f = 3537.43f;

        char c1 = 'a';
        char c2 = '#';
        char c3 = '4';

        boolean b1 = true;
        boolean b2 = false;

    }
}
